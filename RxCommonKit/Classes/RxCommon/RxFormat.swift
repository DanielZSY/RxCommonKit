
import UIKit

/// 时间格式化
public struct RxFormat {
    /// MM/dd/yyyy HH:mm
    public static let yyyyMMddHHmm: String = "yyyy-MM-dd HH:mm"
    /// MM/dd/yyyy HH:mm:ss
    public static let yyyyMMddHHmmss: String = "yyyy-MM-dd HH:mm:ss"
    /// MM/dd/yyyy
    public static let yyyyMMdd: String = "yyyy-MM-dd"
    /// MM/dd
    public static let MMdd: String = "MM-dd"
    /// HH:mm:ss
    public static let HHmmss: String = "HH:mm:ss"
    /// HH:mm
    public static let HHmm: String = "HH:mm"
}
